def twoSum(numbers: list[int], target: int) -> list[int]:
    ret = []
    if target  < numbers[0] + numbers[1] or target > numbers[-1] + numbers[-2]:
        return ret
    l=0
    r=len(numbers) - 1
    while l<r:
        if numbers[l] + numbers[r] > target:
            r = r - 1
        elif numbers[l] + numbers[r] < target:
            l = l +1
        else:
            ret=[l+1,r+1]
            break
    
    return ret


